mensagem = "    eu adoro comida Caseira"
print(mensagem.lower()) # coloca tudo em minúsculo
print(mensagem.upper()) # coloca tudo em maiúsculo
print(mensagem.capitalize()) # primeira letra em maiúscula

print(mensagem.find("c")) # busca a posição da letra 

print(mensagem.replace("a" , "e")) # troca a 1° parâmentro (a), pelo 2° parâmetro (e)

print(mensagem.strip()) # remove os espaços que existirem antes do 1° caractere

lista = [16, 14, 63, 65, 17, 99, 70, 11, 20, 48, 79, 32, 17, 89, 12, 25, 66]

#A leitura do tamanho da lista

tamanho_lista = len(lista)
print(tamanho_lista)


#A leitura do maior e menor valor

maior = max(lista)
menor = min(lista)
print(maior)
print(menor)

#A soma dos valores da lista

soma = sum(lista)
print(soma)

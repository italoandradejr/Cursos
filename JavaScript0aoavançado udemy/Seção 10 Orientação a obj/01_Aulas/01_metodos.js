/*
    *Propriedades que servem como funções;
    *Ou seja, as ações dos objetos;
    *Invocamos os métodos da mesma maneira que funções;
*/

const cachorro = {
    uivar: function () {
        console.log("Auuuuuuuu")
    },
    rosnar: function () {
        console.log("Grrrrr")
    }
}

cachorro.uivar()
cachorro.rosnar()